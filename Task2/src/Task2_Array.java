    public static boolean Q1(int[][] mdg) {
        // Null
        if (mdg == null) {
            return false;
        }
        // Empty
        if (mdg.length <= 1) {
            return false;
        }
        // Squared
        for (int x = 0; x < mdg.length; x++) {
        	// ADDED THIS
        	if (mdg[x] == null) {
        		return false;
        	}
            if (mdg[x].length != mdg.length) {
                return false;
            }
        }
        // Binary
        for (int i = 0; i < mdg.length; i++) {
            for (int j = 0; j < mdg.length; j++) {
                if (mdg[i][j] != 0 && mdg[i][j] != 1) {
                    return false;
                }
                // UNICODE COMMENTED OUT USE IF NEEDED
                /*
				if (mdg[i][j] == '\u0000') {
					return false;
				}
				if (mdg[i][j] == '\u0001') {
					return false;
				}
                */
            }
        }
        // Symmetry
        for (int i = 0; i < mdg.length; i++) {
            if (mdg[i][i] != 0) {
                return false;
            }
        }
        // Inverse
        for (int i = 0; i < mdg.length; i++) {
            for (int j = 0; j < mdg.length; j++) {
                if (mdg[i][j] != mdg[j][i]) {
                    return false;
                }
            }
        }
        return true;
    }
    public static int[] Q2R(int n) {
        if (String.valueOf(n).equals(null)){
        	return null;
        }
        if (n < 1) {
        	return null;
        }
        int[] cluster = new int[n];

        for (int i = 0; i < n; i++) {
            cluster[i] = i+1;
        }
        for (int i = 0; i < cluster.length; i++) {
            int randomIndexToSwap = rand.UI_Int(0,cluster.length - 1);
            int temp = cluster[randomIndexToSwap];
            cluster[randomIndexToSwap] = cluster[i];
            cluster[i] = temp;
        }
        System.out.println(Arrays.toString(cluster));
        return cluster;
    }

    public static int Q3(int[][] mdg, int[] C) {
        if (!Q1(mdg)) {
            return -1;
        }
        if (C == null) {
            return -1;
        }
        if (mdg.length != C.length) {
            return -1;
        }
        for (int i = 0; i < C.length; i++) {
            if (C[i] < 1) {
            	return -1;
            }
        }
        int evm = 0;
        int c1;
        int c2;
        for (int j = 0; j < mdg.length-1; j++) {
            for (int k = j + 1; k < mdg.length; k++) {
                c1 = C[j];
                c2 = C[k];
                if (c1 == c2) {
                    evm = evm + 2 * mdg[j][k] - 1;
                }
            }
        }
        return evm;
    }

