    public static int Q3(int[][] mdg, ArrayList<Integer> C) {
        // Ensures our mdg is valid
        if (!Q1(mdg)) {
            return -1;
        }
        // Check if clustering arrangement is null
        if (C == null) {
            return -1;
        }
        // Checks that our clustering arrangement size matches our mdg length (BE CAREFUL WITH THIS IT MIGHT NEED TO BE IN Q2)
        if (mdg.length != C.size()) {
            return -1;
        }
        // Checks that each item in the clustering arrangement is not less than 1
        for (int i = 0; i < C.size(); i++) {
            if (C.get(i) < 1) {
                return -1;
            }
        }
        // Fitness algorithm
        int evm = 0;
        int c1;
        int c2;
        for (int j = 0; j < mdg.length - 1; j++) {
            for (int k = j+1; k < mdg.length; k++) {
                c1 = C.get(j);
                c2 = C.get(k);
                if (c1 == c2) {
                    evm = evm + 2*mdg[j][k] - 1;
                }
            }
        }
        return evm;
    }
    public static ArrayList<Integer> Q4(ArrayList<Integer> C)
    {
        // Checks that our clustering arrangement isn't null
        if (C == null) {
            System.out.println("Null");
            return null;
        }
        // Checks that our clustering arrangement isn't empty
        if (C.size() == 0) {
            System.out.println("Size too small");
            return null;
        }
        // Checks that each item in our clustering arrangement isn't less than 1
        for (int i = 0; i < C.size(); i++) {
            if (C.get(i) < 1) {
                System.out.println("Negative");
                return null;
            }
        }
        /* Generates a random number in-between 1 and the size of the clustering arrangement - 1, this is because the brief specifies that this value must be less than size of the arrangement
         * Generates a random index in which a random number can be placed
         * The index must be from 0 (because our starting index is 0) to the size of that cluster minus 1 (because if you didn't subtract 1 you would cause an out of bounds exception)
         */
        ArrayList<Integer> ClusteringArrangment = (ArrayList<Integer>) C.clone();
        int number_size = ClusteringArrangment.size();
        int number = rand.UI_Int(1, number_size - 1);
        int index = rand.UI_Int(0, number_size - 1);
        // If the random value is the same as the number at that random index, regenerate a random value for that index
        while (ClusteringArrangment.get(index).equals(number)) {
            number = rand.UI_Int(1, number_size - 1);
        }
        // Replace random value with the pre-existing value at that index
        ClusteringArrangment.set(index, number);
        System.out.println(ClusteringArrangment);
        return ClusteringArrangment;
    }
