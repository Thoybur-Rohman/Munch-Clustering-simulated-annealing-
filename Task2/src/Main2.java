    public static ArrayList<Integer> Q5(int M[][], int ITER)
    {
        // Ensures that ITER is not null
        if ((String.valueOf(ITER).equals(null))) {
            return null;
        }
        // Ensures that ITER isn't below 1
        if (ITER < 1) {
            return null;
        }
        // Q1 validates the given mdg
        if (Q1(M) == false)
        {
            return null;
        }
        // C will act as the current clustering arrangement
        ArrayList<Integer> C = Q2R(M.length);
        // Calculate fitness of current clustering arrangement
        int currentFitness = Q3(M, C);
        /*
         * For each iteration, generate a small change in the current clustering arrangement
         * Calculate fitness of that new clustering arrangement
         * If the fitness is higher than the one previous, replace current clustering arrangement with the new one
         */
        for (int x = 1; x < ITER; x++)
        {
            ArrayList<Integer> small_change = Q4(C);
            int newFitness = Q3(M, small_change);
            System.out.println("Current fitness: " + currentFitness);
            System.out.println("New fitness: " + newFitness);
            if ( newFitness > currentFitness)
            {
                C = (ArrayList<Integer>) small_change.clone();
                currentFitness = newFitness;
            }
            System.out.println(C);
        }
        System.out.println(currentFitness);
        return C;
    }



